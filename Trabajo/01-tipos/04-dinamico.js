// Este archivo muestra cómo funcionan los tipos dinámicos en JavaScript
let numero = 1; // Variable tipo número
let nombre= "Hola Mundo "; // Variable tipo string
let verdadero = true; // Variable tipo booleano (true)
let undef; // Variable sin valor asignado (undefined)
let nulo = null; // Variable con valor null

console.log(typeof numero); // Muestra el tipo de la variable numero
console.log(typeof nombre); // Muestra el tipo de la variable nombre
console.log(typeof verdadero); // Muestra el tipo de la variable verdadero
console.log(typeof undef); // Muestra el tipo de la variable undef
console.log(typeof nulo); // Muestra el tipo de la variable nulo